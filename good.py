from scipy.optimize import linprog

# Коэффициенты целевой функции (отрицательные, так как linprog решает min)
c = [-15, -15, -20]

# Коэффициенты левых частей ограничений (Ax <= b)
A = [
    [2, 10, 25],    # Ограничение по станкам 1 типа
    [10, 5, 3],     # Ограничение по станкам 2 типа
    [120, 85, 50],  # Ограничение по пряже
    [10, 15, 20]    # Ограничение по красителям
]

# Правая часть ограничений
b = [30000, 45000, 30000, 12000]

# Ограничения переменных (x1, x2, x3 >= 0)
x_bounds = [(0, None), (0, None), (0, None)]

# Решение задачи линейного программирования
result = linprog(c, A_ub=A, b_ub=b, bounds=x_bounds, method="highs")

# Вывод результата
if result.success:
    print("Оптимальный выпуск продукции (м):")
    print(f"Ткань 1: {result.x[0]:.2f} м")
    print(f"Ткань 2: {result.x[1]:.2f} м")
    print(f"Ткань 3: {result.x[2]:.2f} м")
    print(f"Максимальная прибыль: {-result.fun:.2f} тг")
else:
    print("Оптимальное решение не найдено.")